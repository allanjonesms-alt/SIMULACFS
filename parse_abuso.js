const lines = text.split('\n').map(l => l.trim()).filter(l => l);

const questions = [];
let currentQuestion = null;

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  
  if (line.startsWith('A)') || line.startsWith('B)') || line.startsWith('C)') || line.startsWith('D)') || line.startsWith('/ B)')) {
    if (currentQuestion) {
      let optText = line.substring(line.indexOf(')') + 1).trim();
      currentQuestion.options.push(optText);
    }
  } else if (line.startsWith('Justificativa:')) {
    if (currentQuestion) {
      currentQuestion.justification = line.substring('Justificativa:'.length).trim();
      
      const match = currentQuestion.justification.match(/A resposta correta é a ([A-D])/);
      if (match) {
        const letter = match[1];
        currentQuestion.correctOption = letter.charCodeAt(0) - 65;
      }
      
      currentQuestion.law = "Leis Extravagantes";
      currentQuestion.category = "Lei de Abuso de Autoridade (Lei 13.869/2019)";
      
      questions.push(currentQuestion);
      currentQuestion = null;
    }
  } else {
    if (!currentQuestion) {
      currentQuestion = {
        text: line,
        options: []
      };
    } else {
      currentQuestion.text += ' ' + line;
    }
  }
}

fs.writeFileSync('src/questions_abuso_autoridade.json', JSON.stringify(questions, null, 2));
console.log('Successfully created questions_abuso_autoridade.json with ' + questions.length + ' questions.');
